import { dag, Container, Directory, Secret, object, func } from "@dagger.io/dagger"

type DeploymentConfig = {
  backendBucket: string
  backendPrefix: string
  gcpProject: string
  cloudflareZoneId: string
  gcpZone: string
  domain: string
  instanceName: string
  deploymentSlot: string
  manageDirectDnsRecords: boolean
}

/**
 * Operator contract (PLAN-180):
 *
 *   bootstrap  Phase 0 — prerequisites only. Creates SA, GCS bucket, SSH keypair.
 *              Never touches compute or DNS. Idempotent. Elevated authority required.
 *   plan       Phase 1 — non-mutating: terraform plan, print diff, exit
 *   check      Phase 1 — ephemeral-only: Terratest suite, create/destroy test resources
 *   deploy     Phase 2 — production-mutating: terraform apply + smallweb SSH bootstrap
 *   verify     Phase 3 — non-mutating: external smoke checks against tidelands.dev
 *   destroy    Phase 2 — production-mutating: terraform destroy
 *
 * Secrets contract (PLAN-181, updated PLAN-192):
 *
 *   gcpCredentials  — GCP service account JSON key. OPTIONAL: omit to use host
 *                     ADC (gcloud volume mount). Required in CI/key-fallback mode.
 *   cloudflareToken — Cloudflare API token (DNS edit + zone settings). Always required.
 *   sshPublicKey    — SSH public key → instance metadata. Generated by bootstrap if absent.
 *   sshPrivateKey   — SSH private key for SSH bootstrap. Generated by bootstrap if absent.
 *
 * GCP auth modes (PLAN-192):
 *   Preferred  — host ADC via ~/.config/gcloud volume mount. bootstrap() creates
 *                the deploy SA and returns a JSON key for subsequent commands.
 *   Fallback   — pass gcpCredentials explicitly (CI or pre-minted key path).
 *
 * Non-secret deployment config is loaded once from env and passed down as a
 * typed object. Dagger owns the workflow surface; Terraform owns the raw vars.
 */
@object()
export class TidelaneInfra {
  /**
   * bootstrap — control-plane prerequisites only (Phase 0).
   *
   * Creates the prerequisites that plan/deploy require, without touching
   * any Terraform-owned resources (compute, DNS, firewall).
   *
   * What it may create:
   *   - GCP service account for deploy operations
   *   - IAM bindings on that SA
   *   - GCS bucket for Terraform state
   *   - Service account JSON key
   *   - SSH keypair for instance access
   *
   * All operations are idempotent. Failures include the exact remediation
   * command needed to resolve the issue.
   *
   * Implemented in PLAN-193.
   */
  @func()
  async bootstrap(
    gcpProject: string,
    cloudflareToken: Secret,
    gcpRegion = "us-central1",
    serviceAccountName = "tidelane-deploy",
    backendBucket = "",
  ): Promise<string> {
    // TODO(PLAN-193): implement via gcloud prerequisite creation
    throw new Error("bootstrap not yet implemented — see PLAN-193")
  }

  /**
   * plan — non-mutating.
   * Runs `terraform plan` and prints the diff. Never writes state.
   */
  @func()
  async plan(
    src: Directory,
    cloudflareToken: Secret,
    sshPublicKey: Secret,
    deploymentSlot = "blue",
    manageDirectDnsRecords = true,
    gcpCredentials?: Secret,
  ): Promise<string> {
    const config = this.loadDeploymentConfig({ deploymentSlot, manageDirectDnsRecords })
    return this.tfInit(src, this.resolveGcpCredentials(gcpCredentials), cloudflareToken, sshPublicKey, config)
      .withExec([
        "terraform", "plan",
        ...this.tfVars(config),
      ])
      .stdout()
  }

  /**
   * deploy — production-mutating.
   * Runs `terraform apply`, emits outputs as JSON.
   * Post-apply smallweb bootstrap is handled in PLAN-186.
   */
  @func()
  async deploy(
    src: Directory,
    cloudflareToken: Secret,
    sshPublicKey: Secret,
    deploymentSlot = "blue",
    manageDirectDnsRecords = true,
    gcpCredentials?: Secret,
  ): Promise<string> {
    const config = this.loadDeploymentConfig({ deploymentSlot, manageDirectDnsRecords })
    return this.tfInit(src, this.resolveGcpCredentials(gcpCredentials), cloudflareToken, sshPublicKey, config)
      .withExec([
        "terraform", "apply", "-auto-approve",
        ...this.tfVars(config),
      ])
      .withExec(["terraform", "output", "-json"])
      .stdout()
  }

  /**
   * destroy — production-mutating.
   * Runs `terraform destroy` against production state.
   */
  @func()
  async destroy(
    src: Directory,
    cloudflareToken: Secret,
    sshPublicKey: Secret,
    deploymentSlot = "blue",
    manageDirectDnsRecords = true,
    gcpCredentials?: Secret,
  ): Promise<string> {
    const config = this.loadDeploymentConfig({ deploymentSlot, manageDirectDnsRecords })
    return this.tfInit(src, this.resolveGcpCredentials(gcpCredentials), cloudflareToken, sshPublicKey, config)
      .withExec([
        "terraform", "destroy", "-auto-approve",
        ...this.tfVars(config),
      ])
      .stdout()
  }

  /**
   * verify — non-mutating.
   * Hits the domain externally and asserts a 200 response.
   * Extended smoke checks are implemented in PLAN-187.
   */
  @func()
  async verify(domain = "tidelands.dev"): Promise<string> {
    return dag.container()
      .from("curlimages/curl:latest")
      .withExec(["curl", "-sf", "--max-time", "15", `https://${domain}`])
      .stdout()
  }

  /**
   * check — ephemeral-only (net non-mutating).
   * Runs Terratest suite against isolated resources named tidelane-test-<hex>.
   * Always destroys on exit. Pass preserveOnFailure=true to skip destroy on failure.
   * Full suite implemented in PLAN-185.
   */
  @func()
  async check(
    src: Directory,
    cloudflareToken: Secret,
    preserveOnFailure = false,
    gcpCredentials?: Secret,
  ): Promise<string> {
    const resolvedGcpCredentials = this.resolveGcpCredentials(gcpCredentials)
    const gcpProject = this.requiredEnv("GCP_PROJECT")
    let ctr = dag.container()
      .from("golang:1.22-bookworm")
      .withDirectory("/workspace", src)
      .withSecretVariable("CLOUDFLARE_API_TOKEN", cloudflareToken)

    if (resolvedGcpCredentials !== null) {
      ctr = ctr.withSecretVariable("GOOGLE_CREDENTIALS", resolvedGcpCredentials)
    } else {
      ctr = ctr.withMountedDirectory(
        "/root/.config/gcloud",
        dag.host().directory(`${process.env.HOME}/.config/gcloud`),
      )
    }

    return ctr
      .withEnvVariable("GCP_PROJECT", gcpProject)
      .withEnvVariable("PRESERVE_ON_FAILURE", preserveOnFailure ? "1" : "0")
      .withWorkdir("/workspace/test")
      .withExec(["go", "test", "-v", "-timeout", "30m", "./..."])
      .stdout()
  }

  // ---------------------------------------------------------------------------
  // Private helpers
  // ---------------------------------------------------------------------------

  /**
   * tfInit returns a container with Terraform initialised against the GCS backend.
   *
   * gcpCredentials: when null, mounts host gcloud config for ADC. When provided,
   * sets GOOGLE_CREDENTIALS directly (key-fallback / CI mode).
   */
  private tfInit(
    src: Directory,
    gcpCredentials: Secret | null,
    cloudflareToken: Secret,
    sshPublicKey: Secret,
    config: DeploymentConfig,
  ): Container {
    let ctr = dag.container()
      .from("hashicorp/terraform:1.7")
      .withDirectory("/workspace", src)
      .withSecretVariable("CLOUDFLARE_API_TOKEN", cloudflareToken)
      .withSecretVariable("TF_VAR_cloudflare_api_token", cloudflareToken)
      .withSecretVariable("TF_VAR_ssh_public_key", sshPublicKey)

    if (gcpCredentials !== null) {
      // Key-fallback mode: explicit service account JSON key.
      ctr = ctr.withSecretVariable("GOOGLE_CREDENTIALS", gcpCredentials)
    } else {
      // Host-auth (ADC) mode: mount the host gcloud config so the GCP SDK and
      // Terraform provider resolve credentials via Application Default Credentials.
      ctr = ctr.withMountedDirectory(
        "/root/.config/gcloud",
        dag.host().directory(`${process.env.HOME}/.config/gcloud`),
      )
    }

    return ctr
      .withWorkdir("/workspace/terraform")
      .withExec([
        "terraform", "init", "-reconfigure",
        `-backend-config=bucket=${config.backendBucket}`,
        `-backend-config=prefix=${config.backendPrefix}`,
      ])
  }

  private tfVars(config: DeploymentConfig): string[] {
    return [
      `-var=gcp_project_id=${config.gcpProject}`,
      `-var=gcp_zone=${config.gcpZone}`,
      `-var=domain=${config.domain}`,
      `-var=cloudflare_zone_id=${config.cloudflareZoneId}`,
      `-var=instance_name=${config.instanceName}`,
      `-var=deployment_slot=${config.deploymentSlot}`,
      `-var=manage_direct_dns_records=${config.manageDirectDnsRecords}`,
    ]
  }

  private loadDeploymentConfig(overrides: Pick<DeploymentConfig, "deploymentSlot" | "manageDirectDnsRecords">): DeploymentConfig {
    return {
      backendBucket: this.requiredEnv("BACKEND_BUCKET"),
      backendPrefix: this.resolveBackendPrefix(overrides.deploymentSlot),
      gcpProject: this.requiredEnv("GCP_PROJECT"),
      cloudflareZoneId: this.requiredEnv("CLOUDFLARE_ZONE_ID"),
      gcpZone: process.env.GCP_ZONE ?? "us-central1-a",
      domain: process.env.DOMAIN ?? "tidelands.dev",
      instanceName: process.env.INSTANCE_NAME ?? "tidelane-smallweb",
      deploymentSlot: overrides.deploymentSlot,
      manageDirectDnsRecords: overrides.manageDirectDnsRecords,
    }
  }

  private resolveBackendPrefix(deploymentSlot: string): string {
    const explicit = process.env.BACKEND_PREFIX
    if (explicit && explicit.trim() !== "") {
      return explicit.replaceAll("{slot}", deploymentSlot)
    }

    const root = process.env.BACKEND_PREFIX_ROOT
    if (root && root.trim() !== "") {
      return `${root.replace(/\/+$/, "")}/${deploymentSlot}/terraform.tfstate`
    }

    throw new Error(
      "missing required env BACKEND_PREFIX or BACKEND_PREFIX_ROOT for Terraform state prefix",
    )
  }

  private requiredEnv(name: string): string {
    const value = process.env[name]
    if (!value || value.trim() === "") {
      throw new Error(`missing required env ${name}`)
    }

    return value
  }

  /**
   * Prefer an explicitly-passed secret, then fall back to Render/CI env.
   * This keeps the module callable both from local CLI and from a preview service
   * whose build command does not pass a dedicated --gcp-credentials flag.
   */
  private resolveGcpCredentials(gcpCredentials?: Secret): Secret | null {
    if (gcpCredentials) {
      return gcpCredentials
    }

    const envCredentials = process.env.GCP_CREDENTIALS_JSON
    if (envCredentials && envCredentials.trim() !== "") {
      return dag.setSecret("gcp-credentials", envCredentials)
    }

    return null
  }
}
